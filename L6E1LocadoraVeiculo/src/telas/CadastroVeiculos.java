    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
 
        if (txtPlaca.getText().equals("") || txtMarca.getText().equals("") || txtModelo.getText().equals("") || 
                txtCor.getText().equals("") || txtAno.getText().equals("")) {
            
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos", "Messagem", JOptionPane.PLAIN_MESSAGE);
            txtPlaca.requestFocus();
            
        }else   {
            String placa = txtPlaca.getText();
            String marca = txtMarca.getText();
            String modelo = txtModelo.getText();
            String cor = txtCor.getText();
            int ano = Integer.parseInt(txtAno.getText());

            if(botao.equals("novo"))    {
  
                Veiculo veiculo = new Veiculo(placa, marca, modelo, cor, ano);
            
                listaVeiculos.add(veiculo);

                 JOptionPane.showMessageDialog(null, "Veiculo cadastrado com sucesso", "Messagem", JOptionPane.INFORMATION_MESSAGE);

            } else if (botao.equals("editar"))  {
                
                int index = tblVeiculos.getSelectedRow();
                
                listaVeiculos.get(index).setAno(ano);
                listaVeiculos.get(index).setCor(cor);
                listaVeiculos.get(index).setMarca(marca);
                listaVeiculos.get(index).setModelo(modelo);
                listaVeiculos.get(index).setPlaca(placa);
            }
            
            //Carregar dados do veiculo na tabela
            carregarTabelaVeiculos();            
            
            //Limpar os textos
            txtAno.setText("");
            txtCor.setText("");
            txtMarca.setText("");
            txtModelo.setText("");
            txtPlaca.setText("");

            //limpar os combo box
            cbxChamadaVeiculo.setSelectedIndex(0);

            //habilitar ou desabilitar botoes
            btnNovo.setEnabled(true);
            btnPesquisar.setEnabled(true);
            btnEditar.setEnabled(false);
            btnOK.setEnabled(false);
            btnSalvar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnCancelar.setEnabled(false);

            //habilitar ou desabilitar campos de texto
            txtAno.setEnabled(false);
            txtCor.setEnabled(false);
            txtMarca.setEnabled(false);
            txtModelo.setEnabled(false);
            txtPlaca.setEnabled(false);

            //habilitar ou desabilitar combo box
            cbxChamadaVeiculo.setEnabled(false);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void tblVeiculosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVeiculosMouseClicked
        
        int i = tblVeiculos.getSelectedRow();
        
        if (i >= 0 && i < listaVeiculos.size()) {
            Veiculo veiculo = listaVeiculos.get(i);
            txtAno.setText(String.valueOf(veiculo.getAno()));
            txtCor.setText(String.valueOf(veiculo.getCor()));
            txtMarca.setText(String.valueOf(veiculo.getMarca()));
            txtModelo.setText(String.valueOf(veiculo.getModelo()));
            txtPlaca.setText(String.valueOf(veiculo.getPlaca()));

        }
        
        //habilitar ou desabilitar botoes
        btnNovo.setEnabled(true);
        btnPesquisar.setEnabled(true);
        btnEditar.setEnabled(true);
        btnOK.setEnabled(false);
        btnSalvar.setEnabled(false);
        btnExcluir.setEnabled(true);
        btnCancelar.setEnabled(false);

        //habilitar ou desabilitar campos de texto
        txtAno.setEnabled(false);
        txtCor.setEnabled(false);
        txtMarca.setEnabled(false);
        txtModelo.setEnabled(false);
        txtPlaca.setEnabled(false);
        
    }//GEN-LAST:event_tblVeiculosMouseClicked

