    //Carregar a tabela com veiculos da lista
    public void carregarTabelaVeiculos()    {
        DefaultTableModel modelo = new DefaultTableModel(new Object[] {"Placa", "Marca", "Modelo", "Cor", "Ano"}, 0 );
        
        for (int i = 0; i<listaVeiculos.size(); i++)    {
            Object linha[] = new Object[]{listaVeiculos.get(i).getPlaca(),
                                            listaVeiculos.get(i).getMarca(),
                                            listaVeiculos.get(i).getModelo(),
                                            listaVeiculos.get(i).getCor(),
                                            listaVeiculos.get(i).getAno()
                                        };
            
            modelo.addRow(linha);
        }
        
        //tabela recebe modelo
        tblVeiculos.setModel(modelo);
        
        tblVeiculos.getColumnModel().getColumn(0).setPreferredWidth(5);
        tblVeiculos.getColumnModel().getColumn(1).setPreferredWidth(5);
        tblVeiculos.getColumnModel().getColumn(2).setPreferredWidth(5);
        tblVeiculos.getColumnModel().getColumn(3).setPreferredWidth(5);
        tblVeiculos.getColumnModel().getColumn(4).setPreferredWidth(5);        
        
    }

