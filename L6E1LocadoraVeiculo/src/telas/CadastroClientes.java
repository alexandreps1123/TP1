    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (txtCPF.getText().equals("") || txtID.getText().equals("") || txtNome.getText().equals("") || txtRG.getText().equals("") || txtTelefone.getText().equals("")) {
            
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos", "Messagem", JOptionPane.PLAIN_MESSAGE);
            txtID.requestFocus();
            
        }else   {        
            int idCliente = Integer.parseInt(txtID.getText());
            String rg = txtRG.getText();
            String cpf = txtCPF.getText();
            String nome = txtNome.getText();
            String telefone = txtTelefone.getText();

            if(botao.equals("novo"))    {   

                Cliente cliente = new Cliente(idCliente, rg, cpf, nome, telefone);

                listaClientes.add(cliente);
            
                JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso", "Messagem", JOptionPane.INFORMATION_MESSAGE);

            } else if (botao.equals("editar"))  {
                 
                int index = tblClientes.getSelectedRow();
                
                listaClientes.get(index).setCpf(cpf);
                listaClientes.get(index).setIdCliente(idCliente);
                listaClientes.get(index).setNome(nome);
                listaClientes.get(index).setRg(rg);
                listaClientes.get(index).setTelefone(telefone);
                
            }
            
            //Carregar dados do motorista na tabela
            carregarTabelaClientes();
            
            //limpar campos de texto
            txtCPF.setText("");
            txtID.setText("");
            txtNome.setText("");
            txtRG.setText("");
            txtTelefone.setText("");

            //limpar combo box
            cbxChamadaCliente.setSelectedIndex(0);

            //habilitar ou desabilitar botoes
            btnNovo.setEnabled(true);
            btnPesquisar.setEnabled(true);
            btnEditar.setEnabled(false);
            btnOK.setEnabled(false);
            btnSalvar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnCancelar.setEnabled(false);

            //habilitar ou desabilitar campos de texto
            txtCPF.setEnabled(false);
            txtID.setEnabled(false);
            txtNome.setEnabled(false);
            txtRG.setEnabled(false);
            txtTelefone.setEnabled(false);

            //habilitar ou desabilitar combo box
            cbxChamadaCliente.setEnabled(false);
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked

        int i = tblClientes.getSelectedRow();
        
        if (i >= 0 && i < listaClientes.size()) {
            Cliente cliente = listaClientes.get(i);
            txtCPF.setText(String.valueOf(cliente.getCpf()));
            txtID.setText(String.valueOf(cliente.getIdCliente()));
            txtNome.setText(String.valueOf(cliente.getNome()));
            txtRG.setText(String.valueOf(cliente.getRg()));
            txtTelefone.setText(String.valueOf(cliente.getTelefone()));

        }
        
        //habilitar ou desabilitar botoes
        btnNovo.setEnabled(true);
        btnPesquisar.setEnabled(true);
        btnEditar.setEnabled(true);
        btnOK.setEnabled(false);
        btnSalvar.setEnabled(false);
        btnExcluir.setEnabled(true);
        btnCancelar.setEnabled(false);

        //habilitar ou desabilitar campos de texto
        txtCPF.setEnabled(false);
        txtID.setEnabled(false);
        txtNome.setEnabled(false);
        txtRG.setEnabled(false);
        txtTelefone.setEnabled(false);
        
    }//GEN-LAST:event_tblClientesMouseClicked

