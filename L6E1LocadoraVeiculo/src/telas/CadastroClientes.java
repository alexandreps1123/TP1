    //Carregar a tabela com clientes da lista
    public void carregarTabelaClientes()    {
        DefaultTableModel modelo = new DefaultTableModel(new Object[] {"ID", "Nome", "Telefone", "CPF", "RG"}, 0 );
        
        for (int i = 0; i<listaClientes.size(); i++)    {
            Object linha[] = new Object[]{listaClientes.get(i).getIdCliente(),
                                            listaClientes.get(i).getNome(),
                                            listaClientes.get(i).getTelefone(),
                                            listaClientes.get(i).getCpf(),
                                            listaClientes.get(i).getRg()
                                        };
            
            modelo.addRow(linha);
        }
        
        //tabela recebe modelo
        tblClientes.setModel(modelo);
        
        tblClientes.getColumnModel().getColumn(0).setPreferredWidth(5);
        tblClientes.getColumnModel().getColumn(1).setPreferredWidth(5);
        tblClientes.getColumnModel().getColumn(2).setPreferredWidth(5);
        tblClientes.getColumnModel().getColumn(3).setPreferredWidth(5);      
        tblClientes.getColumnModel().getColumn(4).setPreferredWidth(5);      
        
    }

