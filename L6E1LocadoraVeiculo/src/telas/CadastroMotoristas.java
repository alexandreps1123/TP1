    //Carregar a tabela com motoristas da lista
    public void carregarTabelaMotoristas()    {
        DefaultTableModel modelo = new DefaultTableModel(new Object[] {"id", "Nome", "Telefone", "CNH"}, 0 );
        
        for (int i = 0; i<listaMotoristas.size(); i++)    {
            Object linha[] = new Object[]{listaMotoristas.get(i).getIdMotorista(),
                                            listaMotoristas.get(i).getNome(),
                                            listaMotoristas.get(i).getTelefone(),
                                            listaMotoristas.get(i).getCnh()
                                        };
            
            modelo.addRow(linha);
        }
        
        //tabela recebe modelo
        tblMotoristas.setModel(modelo);
        
        tblMotoristas.getColumnModel().getColumn(0).setPreferredWidth(5);
        tblMotoristas.getColumnModel().getColumn(1).setPreferredWidth(5);
        tblMotoristas.getColumnModel().getColumn(2).setPreferredWidth(5);
        tblMotoristas.getColumnModel().getColumn(3).setPreferredWidth(5);      
        
    }

