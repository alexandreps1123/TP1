    private void tblMotoristasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMotoristasMouseClicked

        int i = tblMotoristas.getSelectedRow();
        
        if (i >= 0 && i < listaMotoristas.size()) {
            Motorista motorista = listaMotoristas.get(i);
            txtCnh.setText(String.valueOf(motorista.getCnh()));
            txtId.setText(String.valueOf(motorista.getIdMotorista()));
            txtNome.setText(String.valueOf(motorista.getNome()));
            txtTelefone.setText(String.valueOf(motorista.getTelefone()));

        }
        
        //habilitar ou desabilitar botoes
        btnNovo.setEnabled(true);
        btnPesquisar.setEnabled(true);
        btnEditar.setEnabled(true);
        btnOK.setEnabled(false);
        btnSalvar.setEnabled(false);
        btnExcluir.setEnabled(true);
        btnCancelar.setEnabled(false);

        //habilitar ou desabilitar campos de texto
        txtNome.setEnabled(false);
        txtId.setEnabled(false);
        txtCnh.setEnabled(false);
        txtTelefone.setEnabled(false);
    }//GEN-LAST:event_tblMotoristasMouseClicked

