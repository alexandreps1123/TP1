    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        if (txtCnh.getText().equals("") || txtId.getText().equals("") || txtNome.getText().equals("") || txtTelefone.getText().equals("")) {
            
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos", "Messagem", JOptionPane.PLAIN_MESSAGE);
            txtId.requestFocus();
            
        }else   {
            int idMotorista = Integer.parseInt(txtId.getText());
            String nome = txtNome.getText();
            String telefone = txtTelefone.getText();
            String cnh = txtCnh.getText();

            if(botao.equals("novo"))    {
            
                Motorista motorista = new Motorista(idMotorista, nome, telefone, cnh);
            
                listaMotoristas.add(motorista);

                JOptionPane.showMessageDialog(null, "Motorista cadastrado com sucesso", "Messagem", JOptionPane.INFORMATION_MESSAGE);

            } else if (botao.equals("editar"))  {
                
                int index = tblMotoristas.getSelectedRow();
                
                listaMotoristas.get(index).setCnh(cnh);
                listaMotoristas.get(index).setIdMotorista(idMotorista);
                listaMotoristas.get(index).setNome(nome);
                listaMotoristas.get(index).setTelefone(telefone);
            }
            
            
            
            //Carregar dados do motorista na tabela
            carregarTabelaMotoristas(); 
            
            //limpar campos de texto
            txtNome.setText("");
            txtId.setText("");
            txtCnh.setText("");
            txtTelefone.setText("");

            //limpar combo box
            cbxChamadaMotorista.setSelectedIndex(0);

            //habilitar ou desabilitar botoes
            btnNovo.setEnabled(true);
            btnPesquisar.setEnabled(true);
            btnEditar.setEnabled(false);
            btnOK.setEnabled(false);
            btnSalvar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnCancelar.setEnabled(false);

            //habilitar ou desabilitar campos de texto
            txtNome.setEnabled(false);
            txtId.setEnabled(false);
            txtCnh.setEnabled(false);
            txtTelefone.setEnabled(false);

            //habilitar ou desabilitar combo box
            cbxChamadaMotorista.setEnabled(false);
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

