    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    
        if (txtData.getText().equals("") || txtDestino.getText().equals("") || txtHorarioPartida.getText().equals("") ||
                txtHorarioRetorno.getText().equals("") || txtID.getText().equals("") || txtKMFinal.getText().equals("") || 
                txtKMInicial.getText().equals("") || txtOrigem.getText().equals("") ||
                txtTipo.getText().equals("") || txtValorTotal.getText().equals("")) {
            
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos", "Messagem", JOptionPane.PLAIN_MESSAGE);
            txtID.requestFocus();
                       
        }else   {
            int idChamado = Integer.parseInt(txtID.getText());
            String data = txtData.getText();
            String tipo = txtTipo.getText();
            String origem = txtOrigem.getText();
            String destino = txtDestino.getText();
            float horarioPartida = Float.parseFloat(txtHorarioPartida.getText());
            float horarioRetorno = Float.parseFloat(txtHorarioRetorno.getText());
            double kmInicial = Double.parseDouble(txtKMInicial.getText());
            double kmFinal = Double.parseDouble(txtKMFinal.getText());
            double valorTotal = Double.parseDouble(txtValorTotal.getText());

            if(botao.equals("novo"))    {
            
                Chamado chamado = new Chamado(idChamado, data, tipo, origem, destino, horarioPartida, horarioRetorno, kmInicial, kmFinal, valorTotal);

                listaChamados.add(chamado);

                JOptionPane.showMessageDialog(null, "Chamado cadastrado com sucesso", "Messagem", JOptionPane.INFORMATION_MESSAGE);
        
            } else if (botao.equals("editar"))  {
                
                int index = tblChamadas.getSelectedRow();
                
                listaChamados.get(index).setIdChamado(idChamado);
                listaChamados.get(index).setData(data);
                listaChamados.get(index).setTipo(tipo);
                listaChamados.get(index).setOrigem(origem);
                listaChamados.get(index).setDestino(destino);
                listaChamados.get(index).setHorarioPartida(horarioPartida);
                listaChamados.get(index).setHorarioRetorno(horarioRetorno);
                listaChamados.get(index).setKmInicial(kmInicial);
                listaChamados.get(index).setKmFinal(kmFinal);
                listaChamados.get(index).setValorTotal(valorTotal);

            }
            
                
            
            //Carregar dados da chamada na tabela
            carregarTabelaChamados();


            //Limpar os textos
            txtData.setText("");
            txtDestino.setText("");
            txtHorarioPartida.setText("");
            txtHorarioRetorno.setText("");
            txtID.setText("");
            txtKMFinal.setText("");
            txtKMInicial.setText("");
            txtOrigem.setText("");
            txtTipo.setText("");
            txtValorTotal.setText("");

            //limpar os combo box
            cbxCliente.setSelectedIndex(0);
            cbxMotorista.setSelectedIndex(0);
            cbxVeiculo.setSelectedIndex(0);

            //habilitar ou desabilitar botoes
            btnNovo.setEnabled(true);
            btnPesquisar.setEnabled(true);
            btnEditar.setEnabled(false);
            btnOK.setEnabled(false);
            btnSalvar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnCancelar.setEnabled(false);

            //habilitar ou desabilitar campos de texto
            txtData.setEnabled(false);
            txtDestino.setEnabled(false);
            txtHorarioPartida.setEnabled(false);
            txtHorarioRetorno.setEnabled(false);
            txtID.setEnabled(false);
            txtKMFinal.setEnabled(false);
            txtKMInicial.setEnabled(false);
            txtOrigem.setEnabled(false);
            txtTipo.setEnabled(false);
            txtValorTotal.setEnabled(false);

            //habilitar ou desabilitar combo box
            cbxCliente.setEnabled(false);
            cbxMotorista.setEnabled(false);
            cbxVeiculo.setEnabled(false);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

