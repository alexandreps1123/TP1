    //Carregar a tabela com chamados da lista
    public void carregarTabelaChamados()    {
        DefaultTableModel modelo = new DefaultTableModel(new Object[] {"ID", "Data", "Tipo", "Origem", "Destino", "Horario Partida", "Horario Retorno", "KM Inicial", "KM Final", "Valor Total"}, 0 );
        
        for (int i = 0; i<listaChamados.size(); i++)    {
            Object linha[] = new Object[]{listaChamados.get(i).getIdChamado(),
                                            listaChamados.get(i).getData(),
                                            listaChamados.get(i).getTipo(),
                                            listaChamados.get(i).getOrigem(),
                                            listaChamados.get(i).getDestino(),
                                            listaChamados.get(i).getHorarioPartida(),
                                            listaChamados.get(i).getHorarioRetorno(),
                                            listaChamados.get(i).getKmInicial(),
                                            listaChamados.get(i).getKmFinal(),
                                            listaChamados.get(i).getValorTotal()
                                        };
            
            modelo.addRow(linha);
        }
        
        //tabela recebe modelo
        tblChamadas.setModel(modelo);
        
        tblChamadas.getColumnModel().getColumn(0).setPreferredWidth(5);
        tblChamadas.getColumnModel().getColumn(1).setPreferredWidth(5);
        tblChamadas.getColumnModel().getColumn(2).setPreferredWidth(5);
        tblChamadas.getColumnModel().getColumn(3).setPreferredWidth(5);
        tblChamadas.getColumnModel().getColumn(4).setPreferredWidth(5);
        tblChamadas.getColumnModel().getColumn(5).setPreferredWidth(5);
        tblChamadas.getColumnModel().getColumn(6).setPreferredWidth(5);
        tblChamadas.getColumnModel().getColumn(7).setPreferredWidth(5);
        tblChamadas.getColumnModel().getColumn(8).setPreferredWidth(5);
        tblChamadas.getColumnModel().getColumn(9).setPreferredWidth(5);
        
        
    }

