public class IfElse	{
	public static void main(String[] args)	{

		System.out.println("Java example IF-ELSE:");		
		int time = 20;

		if (time > 20)	{
			System.out.println("return IF.");
		}	else if (time < 20)	{
			System.out.println("return ELSE-IF.");
		} else	{
			System.out.println("return ELSE.");
		}
	}
}
