    private void btnNovoAlugarCarroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoAlugarCarroActionPerformed
                
        botao = "novo";
        
        //desabilitar e habilitar botoes
        GerenciarBotoes.botaoNovoEditar(btnNovoAlugarCarro, btnSalvarAlugarCarro, btnEditarAlugarCarro, 
                btnCancelarAlugarCarro, btnExcluirAlugarCarro);
        
        //limpar os campos
        GerenciarCampos.limparTodosCampos(txtDataRetiradaAlugarCarro, txtDataDevolucaoAlugarCarro, 
                    cmbClienteAlugarCarro, cmbCarroAlugarCarro);
        
        //desabilitar ou habilitar campos de texto
        GerenciarCampos.opcaoNovoEditar(txtDataRetiradaAlugarCarro, txtDataDevolucaoAlugarCarro, 
                    cmbClienteAlugarCarro, cmbCarroAlugarCarro);
        
        //carregar combo box
        CarregarComboBox.carregarClientes(cmbClienteAlugarCarro, CadastroCliente.listaClientes);
        CarregarComboBox.carregarCarros(cmbCarroAlugarCarro, CadastroCarro.listaCarros);
        
        txtDataRetiradaAlugarCarro.requestFocus();
        
    }//GEN-LAST:event_btnNovoAlugarCarroActionPerformed

    private void btnSalvarAlugarCarroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarAlugarCarroActionPerformed

        int index1 = cmbClienteAlugarCarro.getSelectedIndex();
        int index2 = cmbCarroAlugarCarro.getSelectedIndex();
        
        if(txtDataRetiradaAlugarCarro.getText().equals("") ||
            txtDataDevolucaoAlugarCarro.getText().equals("")) {
            
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos", "Messagem", JOptionPane.PLAIN_MESSAGE);
            
            txtDataRetiradaAlugarCarro.requestFocus();
            
        } else if (index1 == 0) {
            
            //verifica se algum item foi selecionado
            JOptionPane.showMessageDialog(null, "Um cliente deve ser selecionado", "Messagem", JOptionPane.PLAIN_MESSAGE);
            
        } else if (index2 == 0){
        
            //verifica se algum item foi selecionado
            JOptionPane.showMessageDialog(null, "Um veículo deve ser selecionado", "Messagem", JOptionPane.PLAIN_MESSAGE);
            
        } else {
            
            if (botao.equals("novo"))   {
                    
            //cadastrar aluguel de moto
            listaAluguelCarro.add(AlugarVeiculo.cadastrarAluguelCarro(txtDataRetiradaAlugarCarro.getText(), 
                    txtDataDevolucaoAlugarCarro.getText(), CadastroCliente.listaClientes.get(index1 - 1), 
                    CadastroCarro.listaCarros.get(index2 - 1)));
            
            JOptionPane.showMessageDialog(null, "Aluguel cadastrado com sucesso!", "Messagem", JOptionPane.INFORMATION_MESSAGE);
            
            } else if (botao.equals("editar"))  {
                int index = tblAlugarCarro.getSelectedRow();
                
                //editar cliente
                AlugarVeiculo.editarAluguelCarro(listaAluguelCarro.get(index), txtDataRetiradaAlugarCarro.getText(), 
                    txtDataDevolucaoAlugarCarro.getText(), CadastroCliente.listaClientes.get(index1 - 1), 
                    CadastroCarro.listaCarros.get(index2 - 1));
                
                JOptionPane.showMessageDialog(null, "Alterações salvas com sucesso!", "Messagem", JOptionPane.INFORMATION_MESSAGE);
            }
            
            //carregar os dados na tabela de alugar moto
            CarregarTabela.tabelaAluguelCarroLista(tblAlugarCarro, listaAluguelCarro);
            
            //limpar os campos
            GerenciarCampos.limparTodosCampos(txtDataRetiradaAlugarCarro, txtDataDevolucaoAlugarCarro, 
                    cmbClienteAlugarCarro, cmbCarroAlugarCarro);
            
            txtDataRetiradaAlugarCarro.requestFocus();
        }
        
    }//GEN-LAST:event_btnSalvarAlugarCarroActionPerformed

