    private void btnExcluirAlugarCarroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirAlugarCarroActionPerformed

        int index = tblAlugarCarro.getSelectedRow(); 
        
        if (index >= 0 && index < listaAluguelCarro.size()){
            listaAluguelCarro.remove(index);
        }
        
        //carregar os dados na tabela de clientes
        CarregarTabela.tabelaAluguelCarroLista(tblAlugarCarro, listaAluguelCarro);
        
        //limpar os campos
        GerenciarCampos.limparTodosCampos(txtDataRetiradaAlugarCarro, txtDataDevolucaoAlugarCarro, 
                    cmbClienteAlugarCarro, cmbCarroAlugarCarro);
        
        //desabilitar e habilitar botoes
        GerenciarBotoes.iniciarTela(btnNovoAlugarCarro, btnSalvarAlugarCarro, btnEditarAlugarCarro, 
                btnCancelarAlugarCarro, btnExcluirAlugarCarro);
        
    }//GEN-LAST:event_btnExcluirAlugarCarroActionPerformed

    private void tblAlugarCarroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAlugarCarroMouseClicked

        int index = tblAlugarCarro.getSelectedRow();   
        
        if (index >= 0 && index < listaAluguelCarro.size()) {
            
            //pegar dados da lista de motos alugadas
            AlugarVeiculo carrosAlugados = listaAluguelCarro.get(index);
            txtDataRetiradaAlugarCarro.setText(String.valueOf(carrosAlugados.getDataRetirada()));
            txtDataDevolucaoAlugarCarro.setText(String.valueOf(carrosAlugados.getDataDevolucao()));
            cmbClienteAlugarCarro.removeAllItems();
            cmbClienteAlugarCarro.addItem(carrosAlugados.getCliente().getNomeCompleto());
            cmbCarroAlugarCarro.removeAllItems();
            cmbCarroAlugarCarro.addItem(carrosAlugados.getMoto().getPlaca());
            
        }
        
        //desabilitar e habilitar botoes
        GerenciarBotoes.itemTabela(btnNovoAlugarCarro, btnSalvarAlugarCarro, btnEditarAlugarCarro, 
                btnCancelarAlugarCarro, btnExcluirAlugarCarro);
        
        //desabilitar campos de texto
        GerenciarCampos.camposDefault(txtDataRetiradaAlugarCarro, txtDataDevolucaoAlugarCarro, 
                    cmbClienteAlugarCarro, cmbCarroAlugarCarro);
        
    }//GEN-LAST:event_tblAlugarCarroMouseClicked

