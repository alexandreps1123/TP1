    private void btnNovoAlugarMotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoAlugarMotoActionPerformed
        
        botao = "novo";
        
        //desabilitar e habilitar botoes
        GerenciarBotoes.botaoNovoEditar(btnNovoAlugarMoto, btnSalvarAlugarMoto, btnEditarAlugarMoto, 
                btnCancelarAlugarMoto, btnExcluirAlugarMoto);
        
        //limpar os campos
        GerenciarCampos.limparTodosCampos(txtDataRetiradaAlugarMoto, txtDataDevolucaoAlugarMoto, 
                    cmbClienteAlugarMoto, cmbMotoAlugarMoto);
        
        //desabilitar ou habilitar campos de texto
        GerenciarCampos.opcaoNovoEditar(txtDataRetiradaAlugarMoto, txtDataDevolucaoAlugarMoto, 
                    cmbClienteAlugarMoto, cmbMotoAlugarMoto);
        
        //carregar combo box
        CarregarComboBox.carregarClientes(cmbClienteAlugarMoto, CadastroCliente.listaClientes);
        CarregarComboBox.carregarMotos(cmbMotoAlugarMoto, CadastroMoto.listaMotos);
        
        txtDataRetiradaAlugarMoto.requestFocus();
                
    }//GEN-LAST:event_btnNovoAlugarMotoActionPerformed

    private void btnSalvarAlugarMotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarAlugarMotoActionPerformed
        
        int index1 = cmbClienteAlugarMoto.getSelectedIndex();
        int index2 = cmbMotoAlugarMoto.getSelectedIndex();
        
        if(txtDataRetiradaAlugarMoto.getText().equals("") ||
            txtDataDevolucaoAlugarMoto.getText().equals("")) {
            
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos", "Messagem", JOptionPane.PLAIN_MESSAGE);
            
            txtDataRetiradaAlugarMoto.requestFocus();
            
        } else if (index1 == 0) {
            
            //verifica se algum item foi selecionado
            JOptionPane.showMessageDialog(null, "Um cliente deve ser selecionado", "Messagem", JOptionPane.PLAIN_MESSAGE);
            
        } else if (index2 == 0){
        
            //verifica se algum item foi selecionado
            JOptionPane.showMessageDialog(null, "Um veículo deve ser selecionado", "Messagem", JOptionPane.PLAIN_MESSAGE);
            
        } else {
            
            if (botao.equals("novo"))   {
                    
            //cadastrar aluguel de moto
            listaAluguelMoto.add(AlugarVeiculo.cadastrarAluguelMoto(txtDataRetiradaAlugarMoto.getText(), 
                    txtDataDevolucaoAlugarMoto.getText(), CadastroCliente.listaClientes.get(index1 - 1), 
                    CadastroMoto.listaMotos.get(index2 - 1)));
            
            JOptionPane.showMessageDialog(null, "Aluguel cadastrado com sucesso!", "Messagem", JOptionPane.INFORMATION_MESSAGE);
            
            } else if (botao.equals("editar"))  {
                int index = tblAlugarMoto.getSelectedRow();
                
                //editar cliente
                AlugarVeiculo.editarAluguelMoto(listaAluguelMoto.get(index), txtDataRetiradaAlugarMoto.getText(), 
                    txtDataDevolucaoAlugarMoto.getText(), CadastroCliente.listaClientes.get(index1 - 1), 
                    CadastroMoto.listaMotos.get(index2 - 1));
                
                JOptionPane.showMessageDialog(null, "Alterações salvas com sucesso!", "Messagem", JOptionPane.INFORMATION_MESSAGE);
            }
            
            //carregar os dados na tabela de alugar moto
            CarregarTabela.tabelaAluguelMotoLista(tblAlugarMoto, listaAluguelMoto);
            
            //limpar os campos
            GerenciarCampos.limparTodosCampos(txtDataRetiradaAlugarMoto, txtDataDevolucaoAlugarMoto, 
                    cmbClienteAlugarMoto, cmbMotoAlugarMoto);
            
            txtDataRetiradaAlugarMoto.requestFocus();
        }
        
    }//GEN-LAST:event_btnSalvarAlugarMotoActionPerformed

