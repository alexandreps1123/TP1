    private void tblAlugarMotoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAlugarMotoMouseClicked
        
        int index = tblAlugarMoto.getSelectedRow();   
        
        if (index >= 0 && index < listaAluguelMoto.size()) {
            
            //pegar dados da lista de motos alugadas
            AlugarVeiculo motosAlugadas = listaAluguelMoto.get(index);
            txtDataRetiradaAlugarMoto.setText(String.valueOf(motosAlugadas.getDataRetirada()));
            txtDataDevolucaoAlugarMoto.setText(String.valueOf(motosAlugadas.getDataDevolucao()));
            cmbClienteAlugarMoto.removeAllItems();
            cmbClienteAlugarMoto.addItem(motosAlugadas.getCliente().getNomeCompleto());
            cmbMotoAlugarMoto.removeAllItems();
            cmbMotoAlugarMoto.addItem(motosAlugadas.getMoto().getPlaca());
            
        }
        
        //desabilitar e habilitar botoes
        GerenciarBotoes.itemTabela(btnNovoAlugarMoto, btnSalvarAlugarMoto, btnEditarAlugarMoto, 
                btnCancelarAlugarMoto, btnExcluirAlugarMoto);
        
        //desabilitar campos de texto
        GerenciarCampos.camposDefault(txtDataRetiradaAlugarMoto, txtDataDevolucaoAlugarMoto, 
                    cmbClienteAlugarMoto, cmbMotoAlugarMoto);
        
    }//GEN-LAST:event_tblAlugarMotoMouseClicked

    private void btnExcluirAlugarMotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirAlugarMotoActionPerformed
        
        int index = tblAlugarMoto.getSelectedRow(); 
        
        if (index >= 0 && index < listaAluguelMoto.size()){
            listaAluguelMoto.remove(index);
        }
        
        //carregar os dados na tabela de clientes
        CarregarTabela.tabelaAluguelMotoLista(tblAlugarMoto, listaAluguelMoto);
        
        //limpar os campos
        GerenciarCampos.limparTodosCampos(txtDataRetiradaAlugarMoto, txtDataDevolucaoAlugarMoto, 
                    cmbClienteAlugarMoto, cmbMotoAlugarMoto);
        
        //desabilitar e habilitar botoes
        GerenciarBotoes.iniciarTela(btnNovoAlugarMoto, btnSalvarAlugarMoto, btnEditarAlugarMoto, 
                btnCancelarAlugarMoto, btnExcluirAlugarMoto);
        
    }//GEN-LAST:event_btnExcluirAlugarMotoActionPerformed

